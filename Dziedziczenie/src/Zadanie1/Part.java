package Zadanie1;

public class Part
{
    private int id;
    private String producentName;
    private String model;
    private String productSerial;

    public int getId() {return id;}
    public String getProducentName() {return producentName;}
    public String getModel() {return model;}
    public String getProductSerial() {return productSerial;}

    public void setId(int id) {this.id = id;}
    public void setProducentName(String producentName) {this.producentName = producentName;}
    public void setModel(String model) {this.model = model;}
    public void setProductSerial(String productSerial) {this.productSerial = productSerial;}
}
