package Zadanie2;

public class Metrics
{
    public double MeterToCentimeter(double num)
    {
        return num * 100;
    }

    public double MeterToMillimeter(double num)
    {
        return num * 1000;
    }

    public double CentimeterToMeter(double num)
    {
        return num / 100;
    }

    public double MillimeterToMeter(double num)
    {
        return num / 1000;
    }
}
